// ✯ MVC (Model-View-Controller):
// → a ek software architecture pattern hain.

// MVC ek architectural pattern hai jo applications ko teen parts main me divide karta hai:
// Model: Database Logic
// View: UI Logic(React app me create karenge.)
// Controller: Bussiness Logic

// ⚝ kyu MVC ka use kar rahe hain?
// → code ko split karne ke liye karenge.
// → split - code ko alag karna.
// →  Iska use hota hai applications ko structured aur organized rakhne ke liye.

// 1.Model:
// Yeh data aur business logic ko represent karta hai.
// Model database se data fetch karta hai aur manipulations perform karta hai.
// Example: User data, product information, etc.

// 2. View:
// Yeh user interface ko represent karta hai.
// View Model se data le kar user ko display karta hai.
// Example: HTML pages, templates, etc.

// 3. Controller:
// Yeh user inputs ko handle karta hai aur Model aur View ko manage karta hai.
// Controller user requests ko receive karta hai, Model se data fetch karta hai, aur response ko View me render karta hai.
// Example: Form submissions, URL routing, etc.

// ✰ Real-Life Example:
// Socho ek restaurant application:
// Model: Menu aur order details.
// View: Web pages jo menu aur order information ko show karte hain.
// Controller: User inputs (jaise order karna) ko handle karna aur Model ko update karna.
